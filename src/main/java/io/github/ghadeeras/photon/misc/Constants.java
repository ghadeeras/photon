package io.github.ghadeeras.photon.misc;

public class Constants {

    public static double epsilon = 0x0.002P-7;
    public static double halfEpsilon = epsilon / 2;

    public static final double PI = Math.PI;
    public static final double TwoPI = 2 * PI;
    public static final double FourPI = 4 * PI;

    public static final double OneByPI = 1 / PI;
    public static final double OneByTwoPI = 1 / TwoPI;
    public static final double OneByFourPI = 1 / FourPI;

}
